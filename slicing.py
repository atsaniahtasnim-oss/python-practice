#slicing
b = "Hello, World!"
print(b[2:5]) #prints characters from the 3rd to the 5th character (not including the 5th character)

#Slice From the Start
b = "Hello, World!"
print(b[:5]) #prints the first five characters of the string

#Slice To the End
b = "Hello, World!"
print(b[2:]) #prints characters from the 3rd to the end of the string

#Negative Indexing
b = "Hello, World!"
print(b[-5:-2]) #prints characters from the 5th last to the 2nd last character (not including the 2nd last character)