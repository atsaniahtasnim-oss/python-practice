# Data types in Python
x = 5
print(type(x))

# Data type of a string
x = "Hello, World!"
print(type(x))

# Data type of an integer
x = 20
print(type(x))

# Data type of a float
x = 20.5
print(type(x))

# Data type of a complex number
x = 1j
print(type(x))

# Data type of a list
x = ["apple", "banana", "cherry"]
print(type(x))

# Data type of a tuple
x = ("apple", "banana", "cherry")
print(type(x))

#Data type of a range
x = range(6)
print(type(x))

# Data type of a dictionary
x = {"name": "John", "age": 36}
print(type(x))  

# Data type of a set
x = {"apple", "banana", "cherry"}
print(type(x))

#Data type of a frozenset
x = frozenset({"apple", "banana", "cherry"})
print(type(x))

# Data type of a boolean
x = True
print(type(x))

# Data type of a bytes
x = b"Hello"
print(type(x))

# Data type of a bytearray
x = bytearray(5)
print(type(x))

# Data type of a memoryview
x = memoryview(bytes(5))
print(type(x))

# Data type of None
x = None
print(type(x))

